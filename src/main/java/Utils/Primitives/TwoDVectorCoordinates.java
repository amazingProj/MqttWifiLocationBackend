package Utils.Primitives;

public class TwoDVectorCoordinates {
    // property time
    double x;
    double y;
    double radius;

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public TwoDVectorCoordinates(double x, double y) {
        this.x = x;
        this.y = y;
        this.radius = radius;
    }

    public TwoDVectorCoordinates(double x, double y, double radius) {
        this.x = x;
        this.y = y;
        this.radius = radius;
    }
}
